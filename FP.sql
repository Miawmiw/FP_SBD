CREATE TYPE jeniskel AS ENUM ('L','P');

CREATE TYPE jenisspp AS ENUM ('TUNAI','ANGSURAN');

CREATE TABLE mahasiswa(
	nim CHAR(6) NOT NULL,
	nama VARCHAR(50) NOT NULL,
	jenis_kelamin jeniskel NOT NULL,
	alamat VARCHAR(50) NOT NULL,
	tempat_lahir VARCHAR(20) NOT NULL,
	tanggal_lahir DATE NOT NULL,
	no_telp VARCHAR(15) NOT NULL,
	tanggal_daftar DATE NOT NULL,
	gelombang INTEGER NOT NULL,
	jenis_spp jenisspp NOT NULL
);

CREATE TABLE karyawan(
	id_karyawan CHAR(2) NOT NULL,
	nama_karyawan VARCHAR(50) NOT NULL,
	alamat VARCHAR(50) NOT NULL,
	no_telp VARCHAR(15) NOT NULL,
	email VARCHAR(50) NOT NULL,
	password VARCHAR(15) NOT NULL
);

CREATE TABLE spp(
	jenis_spp jenisspp NOT NULL,
	deskripsi VARCHAR(200) NOT NULL
);

CREATE TABLE detail_spp(
	id_spp CHAR(4) NOT NULL,
	jenis_spp jenisspp NOT NULL,
	cara INTEGER,
	gelombang INTEGER NOT NULL,
	bulan INTEGER,
	harga INTEGER NOT NULL
);

CREATE TABLE pembayaran(
	id_pembayaran INTEGER NOT NULL,
	nim CHAR(6) NOT NULL,
	id_spp CHAR(4) NOT NULL,
	id_karyawan CHAR(3) NOT NULL,
	tanggal_pembayaran DATE NOT NULL
);

CREATE TABLE jadwal_pendaftaran(
	gelombang INTEGER NOT NULL,
	total_biaya_spp INTEGER NOT NULL,
	tgl_pendaftaran_awal DATE NOT NULL,
	tgl_pendaftaran_akhir DATE NOT NULL,
	ujian DATE NOT NULL,
	pengumuman DATE NOT NULL,
	daftar_ulang_awal DATE NOT NULL,
	daftar_ulang_akhir DATE NOT NULL
);

ALTER TABLE jadwal_pendaftaran
	ADD PRIMARY KEY (gelombang);

ALTER TABLE spp
	ADD PRIMARY KEY(jenis_spp);

ALTER TABLE mahasiswa
	ADD PRIMARY KEY(nim),
	ADD FOREIGN KEY(gelombang) REFERENCES jadwal_pendaftaran(gelombang) ON UPDATE CASCADE,
	ADD FOREIGN KEY(jenis_spp) REFERENCES spp(jenis_spp) ON UPDATE CASCADE;

ALTER TABLE karyawan
	ADD PRIMARY KEY(id_karyawan);

ALTER TABLE detail_spp
	ADD PRIMARY KEY(id_spp),
	ADD FOREIGN KEY(gelombang) REFERENCES jadwal_pendaftaran(gelombang) ON UPDATE CASCADE,
	ADD FOREIGN KEY(jenis_spp) REFERENCES spp(jenis_spp) ON UPDATE CASCADE;

ALTER TABLE pembayaran
	ADD PRIMARY KEY(id_pembayaran),
	ADD FOREIGN KEY(nim) REFERENCES mahasiswa(nim) ON UPDATE CASCADE,
	ADD FOREIGN KEY(id_spp) REFERENCES detail_spp(id_spp) ON UPDATE CASCADE,
	ADD FOREIGN KEY(id_karyawan) REFERENCES karyawan(id_karyawan) ON UPDATE CASCADE;
	
